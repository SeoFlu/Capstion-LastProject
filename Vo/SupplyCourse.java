package capstion.fluseo.Vo;

import java.util.Date;

public class SupplyCourse {
    String supplyDate;
    String supplyPlace;
    String supplyStartTime;
    String supplyEndTime;
    String supplyName;

    public SupplyCourse() {
        super();
    }

    public String getSupplyDate() {
        return supplyDate;
    }

    public void setSupplyDate(String supplyDate) {
        this.supplyDate = supplyDate;
    }

    public String getSupplyPlace() {
        return supplyPlace;
    }

    public void setSupplyPlace(String supplyPlace) {
        this.supplyPlace = supplyPlace;
    }

    public String getSupplyStartTime() {
        return supplyStartTime;
    }

    public void setSupplyStartTime(String supplyStartTime) {
        this.supplyStartTime = supplyStartTime;
    }

    public String getSupplyEndTime() {
        return supplyEndTime;
    }

    public void setSupplyEndTime(String supplyEndTime) {
        this.supplyEndTime = supplyEndTime;
    }

    public String getSupplyName() {
        return supplyName;
    }

    public void setSupplyName(String supplyName) {
        this.supplyName = supplyName;
    }
}
